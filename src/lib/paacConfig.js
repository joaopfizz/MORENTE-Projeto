export const SCORE_LABEL = { N: 'Não atende', A: 'Atende', S: 'Supera' };
export const SCORE_VALUE = { N: 0, A: 1, S: 2 };

export function calcScore(scores) {
  const vals = Object.values(scores || {}).filter(Boolean);
  if (!vals.length) return 0;
  const sum = vals.reduce((acc, s) => acc + (SCORE_VALUE[s] ?? 0), 0);
  return Math.round((sum / (vals.length * 2)) * 100);
}

export function generateTasks(scores, config) {
  const tasks = [];
  for (const section of config) {
    for (const sub of section.subsections) {
      for (const crit of sub.criteria) {
        if (scores[crit.key] === 'N') {
          tasks.push({
            id: crypto.randomUUID(),
            criteria_key: crit.key,
            criteria_label: crit.label,
            section_label: `${sub.key} — ${sub.label}`,
            status: 'pendente',
            note: '',
          });
        }
      }
    }
  }
  return tasks;
}
